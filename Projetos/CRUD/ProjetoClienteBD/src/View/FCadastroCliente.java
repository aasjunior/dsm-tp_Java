    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // RECEBER OS VALORES DAS CAIXAS DE TEXTO
        cliente.setCodigo(Integer.parseInt(txtCodigo.getText()));
        cliente.setNome(txtNomeCliente.getText());
        cliente.setTelefone(txtTelefoneCliente.getText());
        cliente.setEmail(txtEmailCliente.getText());
        
        // CHAMAR O MÃTODO CADASTRAR
        cliente.cadastrar();
        
        // MOSTRAR OS DADOS NA TABELA
        ResultSet tabela;
        tabela = null;
        
        // CHAMAR O MÃTODO CONSULTAR
        tabela = cliente.consultar();
        
        DefaultTableModel model = (DefaultTableModel) jtbDadosCliente.getModel();
        model.setNumRows(0);
        
        try{
            do{
                model.addRow(new String[]{tabela.getString(1)});
            }while(tabela.next());
        }catch(SQLException erro){
            JOptionPane.showMessageDialog(null, "Erro ao preencher tabela " + erro);
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

